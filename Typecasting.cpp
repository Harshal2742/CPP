#include<iostream>
using namespace std;

int main(){
    float a=90.65;

    cout<<"10th percentage in the form of float is:"<<a<<endl;
    cout<<"10th percentage by TypeCastig in the form of integer is:"<<int(a)<<endl;//Using typecasting we can convert float into int or any other form.
    cout<<"10th percentage by TypeCastig in the form of integer is:"<<(int)a<<endl;
    
    return 0;
}